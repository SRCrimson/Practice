package gestorAplicacion.Loadout;

public class Pocion {
	String nombre;
	String descripcion; 
	public int curacion;
	public int precio;
	
	public Pocion(String nombre, String descripcion, int curacion, int precio) {
		this.nombre = nombre;
		this.descripcion = descripcion;
		this.curacion = curacion;
		this.precio = precio;
	}
}


